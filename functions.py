def area(width, height):
    result = width * height
    return result
result = area(5, 6)
result_1 = area(3, 4)
result_2 = area(4, 5)
print(result)
print(result_1)
print(result_2)

def subtract(num1, num2):
    result = num1 - num2
    return result
result_3 = subtract(5, 4)
result_4 = subtract(7, 4)
print(result_3)
print(result_4)

def divide(x, y):
    result = x / y
    return result

def add(a, b):
    result = a + b
    return result
result_5 = add(100, 100)
result_6 = add(500, 500)
print(result_5)
print(result_6)